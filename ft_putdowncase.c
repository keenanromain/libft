#include "libft.h"

void	ft_putdowncase(char *s)
{
	int i;

	if (!s)
		return ;
	i = -1;
	while (s[++i])
	{
		if (s[i] <= 90 && s[i] >= 65)
			s[i] = s[i] + 32;
		ft_putchar(s[i]);
	}
}
