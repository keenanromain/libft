#include "libft.h"

int	ft_digitnum(int n)
{
	int	i;

	if (n == -2147483648)
		i = 0;
	else if (n == 0)
		i = 1;
	else
		i = 0;
	while (n != 0)
	{
		n /= 10;
		i++;
	}
	return (i);
}
